package com.gina.pojo.otherpojo;

public class Dog {
    private Double weight;
    private String name;

    public Dog(Double weight) {
        this.weight=weight;
    }

    public Double getWeight() {
        return weight;
    }

    public void setWeight(Double weight) {
        this.weight = weight;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
